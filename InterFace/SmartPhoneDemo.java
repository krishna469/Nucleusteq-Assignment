public class SmartPhoneDemo {
    public static void main(String[] args) {
        // Create SmartPhone object
        SmartPhone myPhone = new SmartPhone();
    
        // Call phone methods
        myPhone.call();
        myPhone.sms();
    
        // Call camera methods
        myPhone.click();
        myPhone.record();
    
        // Call music player methods
        myPhone.play();
        myPhone.stop();
      }
}
